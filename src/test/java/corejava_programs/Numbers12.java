package corejava_programs;

public class Numbers12 {

	public static void main(String[] args) {
		int a = 20;
		
		if (a>0 && a<=10 ) {
			System.out.println("Ten");	
		}
			
		if (a>10 && a<=20 ) {
			System.out.println("Twenty");	
		}
		
		if (a>20 && a<=40 ) {
			System.out.println("40");	
		}
	}


}
